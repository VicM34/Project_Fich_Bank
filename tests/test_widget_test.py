import pytest

from src.widget import get_date, mask_account_card


@pytest.mark.parametrize(
    "input_str, expected",
    [
        # Кредитные карты (16 цифр)
        ("Visa Platinum 1234567812345678", "Visa Platinum 1234 56** **** 5678"),
        ("MasterCard 9876543210987654", "MasterCard 9876 54** **** 7654"),
        ("МИР 1111222233334444", "МИР 1111 22** **** 4444"),
        # Дебетовые карты (16 цифр)
        ("Visa Classic 5555666677778888", "Visa Classic 5555 66** **** 8888"),
        ("Maestro 9999888877776666", "Maestro 9999 88** **** 6666"),
        # Счета (20 цифр)
        ("Счет 12345678901234567890", "Счет **7890"),
        ("Расчетный счет 98765432109876543210", "Расчетный счет **3210"),
        ("Валютный счет 11112222333344445555", "Валютный счет **5555"),
        # Карты с пробелами в номере
        ("Visa Platinum 1234567812345678", "Visa Platinum 1234 56** **** 5678"),
        ("Счет 12345678901234567890", "Счет **7890"),
        # Многословные типы
        ("Зарплатная карта Visa 1234567812345678", "Зарплатная карта Visa 1234 56** **** 5678"),
        ("Корпоративный расчетный счет 12345678901234567890", "Корпоративный расчетный счет **7890"),
    ],
)
def test_mask_account_card_parametrized(input_str: str, expected: str) -> None:
    """Параметризованный тест маскировки карт/счетов."""
    assert mask_account_card(input_str) == expected


@pytest.mark.parametrize(
    "invalid_input, error_message",
    [
        # Неверная длина
        ("Visa 123", "Неверный формат номера"),  # Слишком короткий для карты
        ("Счет 123", "Неверный формат номера"),  # Слишком короткий для счета
        ("Visa 12345678123456789", "Неверный формат номера"),  # 17 цифр
        ("Счет 1234567890123456789", "Неверный формат номера"),  # 19 цифр
        # Не цифры
        ("Visa abcdefghijklmnop", "Неверный формат номера"),
        ("Счет abcdefghijklmnopqrst", "Неверный формат номера"),
        # Неправильный формат строки
        ("1234567812345678", "Строка должна содержать тип и номер"),  # Только номер
        ("", "Строка должна содержать тип и номер"),  # Пустая строка
        ("Visa", "Строка должна содержать тип и номер"),  # Только тип
    ],
)
def test_mask_account_card_invalid_input(invalid_input: str, error_message: str) -> None:
    """Тест обработки некорректных входных данных."""
    with pytest.raises(ValueError, match=error_message):
        mask_account_card(invalid_input)


@pytest.mark.parametrize(
    "date_str, expected",
    [
        # Форматы с миллисекундами
        ("2024-03-11T02:26:18.671407", "11.03.2024"),
        ("2023-12-31T23:59:59.999999", "31.12.2023"),
        ("2020-01-01T00:00:00.000000", "01.01.2020"),
        # Форматы без миллисекунд
        ("2024-03-11T02:26:18", "11.03.2024"),
        ("2023-12-31T23:59:59", "31.12.2023"),
        ("2020-01-01T00:00:00", "01.01.2020"),
        # Граничные даты
        ("2000-01-01T00:00:00.000000", "01.01.2000"),
        ("2030-12-31T23:59:59.999999", "31.12.2030"),
        ("1999-12-31T23:59:59.999999", "31.12.1999"),
    ],
)
def test_get_date_parametrized(date_str: str, expected: str) -> None:
    """Параметризованный тест форматирования даты."""
    assert get_date(date_str) == expected


@pytest.mark.parametrize(
    "invalid_date, error_message",
    [
        # Неполные форматы
        ("2024-03-11", "Неверный формат даты"),  # Без времени
        ("02:26:18.671407", "Неверный формат даты"),  # Только время
        # Неправильные форматы
        ("2024/03/11 02:26:18", "Неверный формат даты"),  # Другой разделитель
        ("11-03-2024T02:26:18", "Неверный формат даты"),  # Неправильный порядок
        ("2024-13-11T02:26:18", "Неверный формат даты"),  # Несуществующий месяц
        ("2024-03-32T02:26:18", "Неверный формат даты"),  # Несуществующий день
        # Совсем не дата
        ("not-a-date", "Неверный формат даты"),
        ("", "Неверный формат даты"),  # Пустая строка
        # Частичные форматы
        ("2024-03-11T02:26", "Неверный формат даты"),  # Без секунд
        ("2024-03-11T02", "Неверный формат даты"),  # Только час
    ],
)
def test_get_date_invalid_formats(invalid_date: str, error_message: str) -> None:
    """Тест обработки невалидных форматов даты."""
    with pytest.raises(ValueError, match=error_message):
        get_date(invalid_date)


@pytest.mark.parametrize(
    "date_str, expected",
    [
        # Базовые случаи
        ("2024-03-11T02:26:18", "11.03.2024"),
        ("2024-03-11T02:26:18.671407", "11.03.2024"),

        # Разные месяцы и дни
        ("2024-01-01T00:00:00", "01.01.2024"),
        ("2024-12-31T23:59:59", "31.12.2024"),

        # Високосный год
        ("2024-02-29T00:00:00", "29.02.2024"),

        # Разные форматы микросекунд
        ("2024-03-11T02:26:18.1", "11.03.2024"),
        ("2024-03-11T02:26:18.123", "11.03.2024"),
        ("2024-03-11T02:26:18.123456", "11.03.2024"),

        # Разные годы
        ("2023-03-11T02:26:18", "11.03.2023"),
        ("2025-03-11T02:26:18", "11.03.2025"),
    ]
)
def test_get_date_valid(date_str: str, expected: str):
    assert get_date(date_str) == expected